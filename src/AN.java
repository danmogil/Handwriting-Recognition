
public class AN {

	private double value;
	private String ref;

	public AN(String ref) {
		this.ref = ref;
	}

	public double getValue() {
		return value;
	}

	public void setValue(double value) {
		this.value = value;
	}

	public String getRef() {
		return ref;
	}

	public void setRef(String ref) {
		this.ref = ref;
	}
}
